function translateValue(value, lang) {
  if (typeof value !== 'string') return value;
  const map = phraseMap[lang];
  if (map.has(value)) return map.get(value);
  return value;
}

function translateObject(obj, lang) {
  if (Array.isArray(obj)) return obj.map((item) => translateObject(item, lang));
  if (obj && typeof obj === 'object') {
    const out = {};
    for (const [key, value] of Object.entries(obj)) {
      out[key] = translateObject(value, lang);
    }
    return out;
  }
  return translateValue(obj, lang);
}

const enDir = path.join(localesDir, 'en');
const files = fs.readdirSync(enDir).filter((f) => f.endsWith('.json'));

for (const lang of ['ar', 'de']) {
  const outDir = path.join(localesDir, lang);
  fs.mkdirSync(outDir, { recursive: true });
  for (const file of files) {
    const enPath = path.join(enDir, file);
    const enJson = JSON.parse(fs.readFileSync(enPath, 'utf8'));
    const outPath = path.join(outDir, file);
    if (file === 'common.json' || file === 'nav.json' || file === 'errors.json') {
      continue;
    }
    const translated = translateObject(enJson, lang);
    fs.writeFileSync(outPath, JSON.stringify(translated, null, 2) + '\n');
  }
}

